package itheimademo1;

public class demo5 {
    public static void main(String[] args) {
       //++ and --
       int a=10;
       a++;
       System.out.println(a);//11
       ++a;
       System.out.println(a);//12

       a--;
       System.out.println(a);//11
       --a;
       System.out.println(a);//10

       
       

    }
    
}
