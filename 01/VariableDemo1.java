public class VariableDemo1 {
    //主入口
    public static void main(String[] args) {
        //定义变量
        //数据类型 变量名 =数据值;
        //数据类型：
        //int整数 double小数
        //变量名：储存空间的名字
        //作用：方便以后使用
        //数据值：存在变量中的数据

        //等号：赋值


        int a =10;
        System.out.println(a);
        int b =20;
        System.out.println(a+b);
        a=50;
        System.out.println(a+b);
        
        int d=100,e=200,f=300;
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);






    }
}
